/**
 * 获取翻译函数
 * @param {string} langCode - 语言代码
 * @returns {function} 翻译函数 t(key)
 */
export function createTranslator(langCode) {
  const dict = translations[langCode] || translations['zh-CN']
  
  return function t(key, params = {}) {
    // 支持点号分隔的路径，如 'nav.dashboard'
    const keys = key.split('.')
    let value = dict
    
    for (const k of keys) {
      if (value && typeof value === 'object' && k in value) {
        value = value[k]
      } else {
        // 如果找不到翻译，返回 key 本身
        console.warn(`Translation missing: ${key} for ${langCode}`)
        return key
      }
    }
    
    let translated = value
    for (const paramKey in params) {
      translated = translated.replace(`{${paramKey}}`, params[paramKey])
    }
    
    return translated
  }
}

/**
 * 获取嵌套对象的值
 * @param {object} obj - 对象
 * @param {string} path - 点号分隔的路径
 * @returns {any} 值
 */
export function getNestedValue(obj, path) {
  return path.split('.').reduce((acc, key) => acc?.[key], obj)
}

